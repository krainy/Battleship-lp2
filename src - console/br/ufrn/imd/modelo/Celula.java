package br.ufrn.imd.modelo;

public class Celula {
	private boolean temBarco;
	private boolean foiAtacada;
	
	public boolean isTemBarco() {
		return temBarco;
	}
	public void setTemBarco(boolean temBarco) {
		this.temBarco = temBarco;
	}
	public boolean isFoiAtacada() {
		return foiAtacada;
	}
	public void setFoiAtacada(boolean foiAtacada) {
		this.foiAtacada = foiAtacada;
	}
	
}
